#A variable is like a container used store data.
#eg.
x=10
name="Kartik"
'''
Here
    'x' is a variable storing the number '10'
    'name' is variable storing string 'Kartik'
'''

#Data Types in python:

#1] int: (store whole numbers without decimal)
age=19  

#2]float: (store Desimal numbers)
pi=3.14

#3]string: (store words,sentence or characters("---"))
name="kartik"

#4]list: ordered collection (A list is a collection of items, it can be changed)
fruits=["banana","apple","mango"]

#5]tuple: Fixed collection (Like a list but cannot be changed after creation)
colors=("red","green","blue")

#6]dict: Dictionary (key-value pairs) used to store related data in key; value format
student={"name": "kartik","age":19,"course": "BCA"}

#7]set: Unorderd unique items (stores a collection of unique values. order is not guaranteed)
numbers={1,2,3,4}

#All together in one example

